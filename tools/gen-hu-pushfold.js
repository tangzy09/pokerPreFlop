'use strict';
/*
 * gen-hu-pushfold.js — compute heads-up (SB vs BB) push/fold Nash for a few
 * stacks and emit BOTH sides: the SB jam range AND the BB call-off range (which
 * the app has never displayed before). Reuses the verified solveRing(nSeats=2).
 * Run: node tools/gen-hu-pushfold.js
 */
const fs = require('node:fs');
const path = require('node:path');
const { buildEqMatrix, solveRing, ringRegret, CLASSES } = require('./pushfold');
const { enforceMonotonic } = require('./monotonic');

const SAMPLES = 4000, SEED = 1234, STACKS = [5, 8, 10, 12, 15, 20, 25];
const SOLVE = { nSeats: 2, iters: 8000, damp: 0.02 };

console.log(`building equity matrix (samples=${SAMPLES})…`);
let t = Date.now();
const EQ = buildEqMatrix(SAMPLES, SEED);
console.log(`  done in ${((Date.now() - t) / 1000).toFixed(1)}s`);

const trim = (raw) => { const m = enforceMonotonic(raw); const o = {}; for (const h of CLASSES) if (m[h] > 0) o[h] = m[h]; return o; };
const pct = (m) => {
  const bc = (l) => (l.length === 2 ? 6 : l[2] === 's' ? 4 : 12);
  let a = 0, b = 0; for (const h of CLASSES) { a += bc(h) * (m[h] || 0); b += bc(h); }
  return a / b;
};

const stacks = {}, exploit = {};
for (const S of STACKS) {
  const ring = solveRing(S, EQ, SOLVE);
  const jam = ring.seats[0].jam;            // SB jam range
  const call = ring.seats[0].callers[1];    // BB call-off range vs SB jam
  exploit[S] = Math.round(ringRegret(S, EQ, ring).maxRegret * 1000) / 1000;
  stacks[S] = { jam: trim(jam), call: trim(call) };
  console.log(`  ${S}bb: SB jam ${(pct(jam) * 100).toFixed(0)}%  BB call ${(pct(call) * 100).toFixed(0)}%  | exploit ${exploit[S]}bb`);
}

const data = {
  meta: { game: 'HU SB-vs-BB', stacks: STACKS, model: 'no-overcall chip-EV Nash equilibrium',
    equity: 'Monte-Carlo class equity', samples: SAMPLES, seed: SEED, exploitability: exploit },
  stacks,
};
const out = path.join(__dirname, '..', 'js', 'data', 'hu-pushfold.js');
fs.writeFileSync(out,
  `/* AUTO-GENERATED by tools/gen-hu-pushfold.js — do not edit by hand.
   Heads-up (SB vs BB) push/fold Nash for ${STACKS.join('/')}bb. Each stack has
   jam (SB shove freq) and call (BB call-off freq vs a jam) per hand.
   Regenerate: node tools/gen-hu-pushfold.js */
const HU_PUSHFOLD = ${JSON.stringify(data, null, 1)};
if (typeof module !== 'undefined') module.exports = HU_PUSHFOLD;
`, 'utf8');
console.log('wrote ' + out);
