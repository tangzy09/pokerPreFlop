'use strict';
/*
 * gen-pushfold.js — compute push/fold Nash and emit it as a plain <script>
 * global the app loads offline (no fetch, file:// friendly). The equity matrix
 * is stack-independent, so we build it once and solve each ring per stack.
 * Emits three things, all from the verified solveRing:
 *   stacks   — 9-max SB-open..UTG jam ranges (push mode)
 *   ring6    — 6-max jam ranges (终桌/短手)
 *   calloff  — 9-max BB call-off range facing a single jammer (callshove mode)
 * Run: node tools/gen-pushfold.js   (regenerate after changing the model)
 */
const fs = require('node:fs');
const path = require('node:path');
const { buildEqMatrix, solveRing, ringRegret, CLASSES } = require('./pushfold');

const SAMPLES = 4000, SEED = 1234;
const STACKS = [8, 10, 12, 15, 20];      // 9-max jam depths
const STACKS6 = [10, 15, 20];            // 6-max jam depths (终桌/短手)
const STACKS_CO = [10, 15, 20];          // 9-max BB call-off depths
const SOLVE = { iters: 8000, damp: 0.02 };
const SEAT_OF = { UTG: 0, MP: 3, CO: 5, BTN: 6, SB: 7 };       // 9-max (BB = 8)
const SEAT_OF6 = { UTG: 0, HJ: 1, CO: 2, BTN: 3, SB: 4 };      // 6-max (SB = 4, BB = 5)
const round = (x) => Math.round(x * 1000) / 1000;
const trim = (m) => { const o = {}; for (const h of CLASSES) if (m[h] > 0) o[h] = round(m[h]); return o; };

console.log(`building ${CLASSES.length}x${CLASSES.length} equity matrix (samples=${SAMPLES})…`);
let t = Date.now();
const EQ = buildEqMatrix(SAMPLES, SEED);                       // stack-independent
console.log(`  matrix done in ${((Date.now() - t) / 1000).toFixed(1)}s`);

// ---- 9-max: jam ranges per seat + BB call-off vs a BTN jam ----
const stacks = {}, exploit = {}, calloff = {};
for (const S of STACKS) {
  const ring = solveRing(S, EQ, { nSeats: 9, ...SOLVE });
  exploit[S] = round(ringRegret(S, EQ, ring).maxRegret);
  const seats = {};
  for (const [name, idx] of Object.entries(SEAT_OF)) seats[name] = trim(ring.seats[idx].jam);
  stacks[S] = { seats };
  if (STACKS_CO.includes(S)) calloff[S] = { btn: trim(ring.seats[SEAT_OF.BTN].callers[8]) }; // BB(8) call vs BTN jam
  console.log(`  9max ${S}bb: ` + Object.entries(SEAT_OF).map(([n, i]) => `${n} ${(ring.seats[i].jamPct * 100).toFixed(0)}%`).join('  ') + `  | exploit ${exploit[S]}bb`);
}

// ---- 6-max: jam ranges per seat ----
const ring6 = {}, exploit6 = {};
for (const S of STACKS6) {
  const ring = solveRing(S, EQ, { nSeats: 6, ...SOLVE });
  exploit6[S] = round(ringRegret(S, EQ, ring).maxRegret);
  const seats = {};
  for (const [name, idx] of Object.entries(SEAT_OF6)) seats[name] = trim(ring.seats[idx].jam);
  ring6[S] = { seats };
  console.log(`  6max ${S}bb: ` + Object.entries(SEAT_OF6).map(([n, i]) => `${n} ${(ring.seats[i].jamPct * 100).toFixed(0)}%`).join('  ') + `  | exploit ${exploit6[S]}bb`);
}

const data = {
  meta: { model: 'no-overcall chip-EV Nash equilibrium', equity: 'Monte-Carlo class equity (combo+board averaged)',
    samples: SAMPLES, seed: SEED, stacks: STACKS, stacks6: STACKS6, seatMap: SEAT_OF, seatMap6: SEAT_OF6,
    exploitability: exploit, exploitability6: exploit6 },
  stacks, ring6, calloff,
};
const out = path.join(__dirname, '..', 'js', 'data', 'pushfold.js');
fs.mkdirSync(path.dirname(out), { recursive: true });
fs.writeFileSync(out,
  `/* AUTO-GENERATED by tools/gen-pushfold.js — do not edit by hand.
   Computed push/fold Nash (${data.meta.model}).
   PUSHFOLD.stacks[d].seats[pos] = 9-max jam freq; PUSHFOLD.ring6[d].seats[pos] = 6-max jam;
   PUSHFOLD.calloff[d].btn = 9-max BB call-off freq vs a BTN jam.
   Regenerate: node tools/gen-pushfold.js */
const PUSHFOLD = ${JSON.stringify(data, null, 1)};
if (typeof module !== 'undefined') module.exports = PUSHFOLD;
`, 'utf8');
console.log('wrote ' + out);
