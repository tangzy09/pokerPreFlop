'use strict';
/*
 * gen-pushfold.js — compute 9-max push/fold Nash for several stack depths and
 * emit them as a plain <script> global the app loads offline (no fetch,
 * file:// friendly). The equity matrix is stack-independent, so we build it
 * once and solve the ring per stack.
 * Run: node tools/gen-pushfold.js   (regenerate after changing the model)
 */
const fs = require('node:fs');
const path = require('node:path');
const { buildEqMatrix, solveRing, CLASSES } = require('./pushfold');

const SAMPLES = 4000, SEED = 1234, NSEATS = 9, STACKS = [10, 15, 20];
// which 9-max seats back our 5 displayed positions (0=UTG .. 6=BTN,7=SB)
const SEAT_OF = { UTG: 0, MP: 3, CO: 5, BTN: 6, SB: 7 };

console.log(`building ${CLASSES.length}x${CLASSES.length} equity matrix (samples=${SAMPLES})…`);
let t = Date.now();
const EQ = buildEqMatrix(SAMPLES, SEED);                       // stack-independent
console.log(`  matrix done in ${((Date.now() - t) / 1000).toFixed(1)}s`);

const stacks = {};
for (const S of STACKS) {
  const ring = solveRing(S, EQ, { nSeats: NSEATS });
  const seats = {};
  for (const [name, idx] of Object.entries(SEAT_OF)) {
    const jam = ring.seats[idx].jam, o = {};
    for (const h of CLASSES) if (jam[h] > 0) o[h] = jam[h];     // shove freq (omit pure folds)
    seats[name] = o;
  }
  stacks[S] = { seats };
  console.log(`  ${S}bb: ` + Object.entries(SEAT_OF).map(([n, i]) => `${n} ${(ring.seats[i].jamPct * 100).toFixed(0)}%`).join('  '));
}

const data = {
  meta: { nSeats: NSEATS, stacks: STACKS, model: 'no-overcall chip-EV Nash equilibrium',
    equity: 'Monte-Carlo class equity (combo+board averaged)', samples: SAMPLES, seed: SEED, seatMap: SEAT_OF },
  stacks,
};
const out = path.join(__dirname, '..', 'js', 'data', 'pushfold.js');
fs.mkdirSync(path.dirname(out), { recursive: true });
fs.writeFileSync(out,
  `/* AUTO-GENERATED by tools/gen-pushfold.js — do not edit by hand.
   Computed ${NSEATS}-max push/fold Nash (${data.meta.model}) for ${STACKS.join('/')}bb.
   PUSHFOLD.stacks[depth].seats[pos] maps hand -> shove frequency.
   Regenerate: node tools/gen-pushfold.js */
const PUSHFOLD = ${JSON.stringify(data, null, 1)};
if (typeof module !== 'undefined') module.exports = PUSHFOLD;
`, 'utf8');
console.log('wrote ' + out);
