'use strict';
/*
 * gen-pushfold.js — compute 10bb 9-max push/fold Nash and emit it as a plain
 * <script> global the app loads offline (no fetch, file:// friendly).
 * Run: node tools/gen-pushfold.js   (regenerate after changing the model)
 */
const fs = require('node:fs');
const path = require('node:path');
const { buildEqMatrix, solveRing, CLASSES } = require('./pushfold');

const SAMPLES = 4000, SEED = 1234, STACK = 10, NSEATS = 9;
// which 9-max seats back our 5 displayed positions (0=UTG .. 6=BTN,7=SB)
const SEAT_OF = { UTG: 0, MP: 3, CO: 5, BTN: 6, SB: 7 };

console.log(`building ${CLASSES.length}x${CLASSES.length} equity matrix (samples=${SAMPLES})…`);
let t = Date.now();
const EQ = buildEqMatrix(SAMPLES, SEED);
console.log(`  matrix done in ${((Date.now() - t) / 1000).toFixed(1)}s; solving ring…`);
const ring = solveRing(STACK, EQ, { nSeats: NSEATS });

const seats = {};
for (const [name, idx] of Object.entries(SEAT_OF)) {
  const jam = ring.seats[idx].jam, o = {};
  for (const h of CLASSES) if (jam[h] > 0) o[h] = jam[h];   // store shove freq (omit pure folds)
  seats[name] = o;
  console.log(`  ${name}: jam% ${(ring.seats[idx].jamPct * 100).toFixed(1)}  (${Object.keys(o).length} hands)`);
}

const data = {
  meta: { stack: STACK, nSeats: NSEATS, model: 'no-overcall chip-EV Nash equilibrium',
    equity: 'Monte-Carlo class equity (combo+board averaged)', samples: SAMPLES, seed: SEED, seatMap: SEAT_OF },
  seats,
};
const out = path.join(__dirname, '..', 'js', 'data', 'pushfold-10bb.js');
fs.mkdirSync(path.dirname(out), { recursive: true });
fs.writeFileSync(out,
  `/* AUTO-GENERATED by tools/gen-pushfold.js — do not edit by hand.
   Computed ${STACK}bb ${NSEATS}-max push/fold Nash (${data.meta.model}).
   Each seat maps hand -> shove frequency. Regenerate: node tools/gen-pushfold.js */
const PUSHFOLD_10BB = ${JSON.stringify(data, null, 1)};
if (typeof module !== 'undefined') module.exports = PUSHFOLD_10BB;
`, 'utf8');
console.log('wrote ' + out);
